type LanguageContextType = {
  language: AppLanguage;
  selectedLanguage: AppLanguage | null;
  isLanguageReady: boolean;
  setLanguage: (lang: AppLanguage) => Promise<void>;
  t: typeof translations.tr;
};

const LanguageContext = createContext<LanguageContextType | null>(null);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>("tr");
  const [selectedLanguage, setSelectedLanguage] = useState<AppLanguage | null>(null);
  const [isLanguageReady, setIsLanguageReady] = useState(false);

  useEffect(() => {
    const loadLanguage = async () => {
      try {
        const saved = await AsyncStorage.getItem(STORAGE_KEY);

        if (saved === "tr" || saved === "en") {
          setLanguageState(saved);
          setSelectedLanguage(saved);
        }
      } catch (error) {
        console.log("Dil yükleme hatası:", error);
      } finally {
        setIsLanguageReady(true);
      }
    };

    loadLanguage();
  }, []);

  const setLanguage = async (lang: AppLanguage) => {
    setLanguageState(lang);
    setSelectedLanguage(lang);
    await AsyncStorage.setItem(STORAGE_KEY, lang);
  };

  const value = useMemo(
    () => ({
      language,
      selectedLanguage,
      isLanguageReady,
      setLanguage,
      t: translations[language],
    }),
    [language, selectedLanguage, isLanguageReady]
  );

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);

  if (!ctx) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }

  return ctx;
}